//Template Literals

let userName = "Mark";
let age = 23;
let country = "United States of America"

console.log("Using + operator")
console.log("I am "+userName+" and I'm "+age+" years old.");

console.log("Using Backticks")
console.log(`I am ${userName} and I'm ${age} years old.`);

console.log("Multi-line Text")
console.log(`I am ${userName}.
I'm ${age} years old.
I live in ${country}`);